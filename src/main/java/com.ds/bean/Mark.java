public class Mark {

    private long num;
    private long cNum;
    private String name;
    private int range;
    private double numMark;
    private int normMark;


    public long getNum() {
        return num;
    }

    public void setNum(long num) {
        this.num = num;
    }

    public long getcNum() {
        return cNum;
    }

    public void setcNum(long cNum) {
        this.cNum = cNum;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getRange() {
        return range;
    }

    public void setRange(int range) {
        this.range = range;
    }

    public double getNumMark() {
        return numMark;
    }

    public void setNumMark(double numMark) {
        this.numMark = numMark;
    }

    public int getNormMark() {
        return normMark;
    }

    public void setNormMark(int normMark) {
        this.normMark = normMark;
    }
}
