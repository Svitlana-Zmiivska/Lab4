package com.titapr.bean;


public class Result {

    private long num;
    private long lNum;
    private long aNum;
    private double range;
    private int aWeight;


    public long getNum() {
        return num;
    }

    public void setNum(long num) {
        this.num = num;
    }

    public long getlNum() {
        return lNum;
    }

    public void setlNum(long lNum) {
        this.lNum = lNum;
    }

    public long getaNum() {
        return aNum;
    }

    public void setaNum(long aNum) {
        this.aNum = aNum;
    }

    public double getRange() {
        return range;
    }

    public void setRange(double range) {
        this.range = range;
    }

    public int getaWeight() {
        return aWeight;
    }

    public void setaWeight(int aWeight) {
        this.aWeight = aWeight;
    }
}
